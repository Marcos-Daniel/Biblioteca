    public void filtrar(String nome,String cpf){
        
        Usuario filtro = new Usuario(0,nome,null,cpf,null,null);
   
        this.busca = (ArrayList<Usuario>) dao.filtrar(filtro);
     
        preencheTabela(busca);
    }   
    
    public void buscarTodos(){
        this.busca = (ArrayList<Usuario>) dao.buscar();
        
        preencheTabela(busca);
    }

    public void preencheTabela(ArrayList<Usuario> busca) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Nome");
        modelo.addColumn("Telefone");
        modelo.addColumn("CPF");
        modelo.addColumn("CEP");
        modelo.addColumn("NÂº ResidÃªncia");
        
        for(Usuario c: busca){
            Vector linha = new Vector();
            linha.add(c.getId());
            linha.add(c.getNome());
            linha.add(c.getTelefone());
            linha.add(c.getCPF());
            linha.add(c.getCEP());
            linha.add(c.getNumeroResidencia());
            modelo.addRow(linha);
        }
        tblUsuarios.setModel(modelo);
    }
    
    private void limparCamposCadastrar(){
        txtNome.setText("");
        txtTelefone.setText("");
        txtCpf.setText("");
        txtCep.setText("");
        txtNResidencia.setText("");
    }

    private void limparCamposFiltrar(){
        txtfNome.setText("");
        txtfCPF.setText("");
    }
    
    private void recuperarCamposCadastrar(){
        String nome = txtNome.getText().trim();
        novo.setNome(nome);
        String telefone = txtTelefone.getText().trim();
        novo.setTelefone(telefone);
        String cpf = txtCpf.getText().trim();
        novo.setCPF(cpf);
        String cep = txtCep.getText().trim();
        novo.setCEP(cep);
        String nResidencia = txtNResidencia.getText().trim();
        novo.setNumeroResidencia(nResidencia);
    }
    
    private void preencheCampos(Usuario usuario){
        novo = usuario;
        txtNome.setText(usuario.getNome());
        txtTelefone.setText(usuario.getTelefone());
        txtCpf.setText(usuario.getCPF());
        txtCep.setText(usuario.getCEP());
        txtNResidencia.setText(usuario.getNumeroResidencia());
    } 
